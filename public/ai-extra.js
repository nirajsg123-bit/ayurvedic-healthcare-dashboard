// ============================================================
//  AyurAI — Extra FREE in-browser features (no API, no key)
//  Handles the browser-only AI Lab tools (TTS, speech-input,
//  export-report) and provides the helper functions they use.
//  Loaded after ai-ui.js.
// ============================================================
(function () {
  var bodyEl = function () { return document.getElementById('ai-tool-body'); };

  // Text-to-Speech (Web Speech API)
  window.ayurTTS = function (text) {
    if (!('speechSynthesis' in window)) { if (window.showToast) showToast('TTS not supported', 'error'); return; }
    try {
      window.speechSynthesis.cancel();
      var u = new SpeechSynthesisUtterance(String(text || '').replace(/[{}"\[\],]/g, ' ').slice(0, 3000));
      u.rate = 0.98; u.pitch = 1; u.lang = 'en-IN';
      var voices = window.speechSynthesis.getVoices();
      var hi = voices && voices.find(function (v) { return /hi/i.test(v.lang); });
      if (hi) u.voice = hi;
      window.speechSynthesis.speak(u);
    } catch (e) { if (window.showToast) showToast('TTS failed', 'error'); }
  };

  // Speech-to-text (Web Speech API)
  window.ayurListen = function (cb) {
    var SR = window.SpeechRecognition || window.webkitSpeechRecognition;
    if (!SR) { if (window.showToast) showToast('Speech input not supported here', 'error'); return; }
    try {
      var r = new SR(); r.lang = 'en-IN'; r.interimResults = false; r.maxAlternatives = 1;
      r.onresult = function (e) { cb(e.results[0][0].transcript); };
      r.onerror = function () { if (window.showToast) showToast('Mic error — type instead', 'error'); };
      r.start();
    } catch (e) { if (window.showToast) showToast('Mic unavailable', 'error'); }
  };

  // Client-side printable / PDF export
  window.ayurExport = function (text, title) {
    var w = window.open('', '_blank');
    if (!w) { if (window.showToast) showToast('Allow popups to export', 'error'); return; }
    var safe = String(text).replace(/&/g, '&amp;').replace(/</g, '&lt;').replace(/>/g, '&gt;');
    w.document.write('<html><head><title>' + (title || 'AyurAI Report') + '</title>' +
      '<style>body{font-family:Segoe UI,Arial;padding:32px;color:#1a1a1a;line-height:1.6}h1{color:#2d5016}pre{white-space:pre-wrap;font-family:inherit}</style></head>' +
      '<body><h1>' + (title || 'AyurAI Health Report') + '</h1><pre>' + safe + '</pre>' +
      '<hr><p style="font-size:11px;color:#888">Generated by AyurAI — educational only, not a medical diagnosis.</p></body></html>');
    w.document.close(); setTimeout(function () { w.print(); }, 400);
  };

  // Dispatcher for the browser-only AI Lab tools
  window.handleBrowserTool = function (f, id) {
    var b = bodyEl(); if (!b) return;
    if (id === 'tts') {
      b.innerHTML = '<div style="background:var(--bg-alt);padding:16px;border-radius:8px;">' +
        '<p style="margin:0 0 10px;">Type or paste any health text and AyurAI will read it aloud — free, no API.</p>' +
        '<textarea id="tts-text" rows="4" placeholder="Paste an answer or any Ayurvedic note…" style="width:100%;padding:10px;border-radius:8px;border:1px solid var(--border);background:var(--bg);color:var(--text);"></textarea>' +
        '<div style="margin-top:10px;display:flex;gap:8px;">' +
        '<button class="btn btn-accent" onclick="ayurTTS(document.getElementById(\'tts-text\').value)">🔊 Read aloud</button>' +
        '<button class="btn btn-secondary" onclick="ayurListen(function(t){document.getElementById(\'tts-text\').value=t;})">🎙️ Speak</button>' +
        '</div></div>';
    } else if (id === 'speech-input') {
      b.innerHTML = '<div style="background:var(--bg-alt);padding:16px;border-radius:8px;">' +
        '<p style="margin:0 0 10px;">Tap speak and say your health question. Free Web Speech recognition (your browser may ask for mic permission).</p>' +
        '<div style="display:flex;gap:8px;">' +
        '<button class="btn btn-accent" onclick="ayurListen(function(t){window.ayurAIChat(t).then(function(a){var m=document.getElementById(\'speech-out\');if(m)m.textContent=a;});})">🎙️ Speak to AyurAI</button>' +
        '</div><div id="speech-out" style="margin-top:12px;font-size:14px;line-height:1.6;"></div></div>';
    } else if (id === 'export-report') {
      b.innerHTML = '<div style="background:var(--bg-alt);padding:16px;border-radius:8px;">' +
        '<p style="margin:0 0 10px;">Paste any AyurAI result / condition plan and export it as a printable PDF — free, client-side.</p>' +
        '<textarea id="exp-text" rows="6" placeholder="Paste a condition bundle, plan or report…" style="width:100%;padding:10px;border-radius:8px;border:1px solid var(--border);background:var(--bg);color:var(--text);"></textarea>' +
        '<button class="btn btn-accent" style="margin-top:10px;" onclick="ayurExport(document.getElementById(\'exp-text\').value, \'AyurAI Health Report\')">📄 Export as PDF</button>' +
        '</div>';
    }
  };
})();
